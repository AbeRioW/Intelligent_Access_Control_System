/**
  * @brief  UART接收完成回调函数
  * @param  huart: UART句柄
  * @retval None
  */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	
		    if (huart == &huart1)
    {
        // 计算实际接收到的字节数
        esp8266_cnt = ESP8266_BUF_SIZE - 1 - huart->RxXferCount;
        if (esp8266_cnt > 0) {
            esp8266_buf[esp8266_cnt] = '\0';
        }
        // 重新启动 DMA 接收
        HAL_UART_Receive_DMA(&huart1, (uint8_t*)esp8266_buf, ESP8266_BUF_SIZE - 1);
    }
    if (huart == &huart3)
    {
        if ((USART3_RX_STA & 0x8000) == 0) // 接收未完成
        {
            if (USART3_RX_STA < USART3_MAX_RECV_LEN) // 缓冲区未满
            {
                USART3_RX_STA++; // 接收长度+1
                // 继续接收下一个字节
                HAL_UART_Receive_IT(&huart3, &USART3_RX_BUF[USART3_RX_STA], 1);
            }
            else
            {
                USART3_RX_STA |= 0x8000; // 标记接收完成
            }
        }
    }
}

